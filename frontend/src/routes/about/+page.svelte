<div class="text-column">
	<h1>Sobre Nosotros - Proyecto Alveus</h1>
	<div class="spacer"></div>
	<p>
		Alveus es una plataforma digital diseñada para proporcionar datos detallados y actualizados sobre los embalses. Nuestro objetivo es ofrecer información accesible sobre los niveles actuales de agua, datos históricos y estadísticas clave a través de gráficos interactivos. Esta herramienta está dirigida a usuarios que deseen monitorear el estado de los embalses cercanos, como investigadores, responsables de la gestión hídrica o cualquier persona interesada en los recursos hídricos.
	</p>
	<div class="spacer"></div>
	<p>
		El proyecto Alveus nació durante el Malakathon, un hackathon enfocado en resolver retos ambientales y tecnológicos. Nuestro equipo, motivado por la necesidad de monitorear eficientemente el estado del agua en los embalses, decidió desarrollar esta solución que integra datos en tiempo real y almacenamiento en la nube para proporcionar análisis y tendencias históricas.

	</p>
	<div class="spacer"></div>
	<p>
		Gracias a la robustez de Oracle Cloud como plataforma de base de datos y hospedaje, Alveus es capaz de manejar grandes volúmenes de datos, asegurando que la información se entregue de manera rápida y confiable. Utilizamos tecnologías modernas como Svelte para la interfaz de usuario y Rust para el backend, lo que nos permite ofrecer un sistema eficiente, seguro y fácil de usar.
	</p>
	<div class="spacer"></div>
	<p>
		En Alveus, creemos que la información es clave para una mejor gestión de los recursos naturales, y por eso nuestro objetivo es crear una herramienta accesible y confiable para todos.
	</p>
